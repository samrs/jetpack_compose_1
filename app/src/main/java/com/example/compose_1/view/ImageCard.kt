class ImageCard() {

    @Preview(showBackground = true, device = Devices.PIXEL_2_XL)
    @Composable
    fun Start() {
        val painter = painterResource(id = R.drawable.darth)
        val description = "This is a darth vader face"
        val title = "Darth Vader"
        Box(
            modifier = Modifier
                .fillMaxWidth(0.5f)
                .padding(16.dp)
        ) {
            ImageCompose(
                painter = painter,
                contentDescription = description,
                title = title
            )
        }
    }

    @Composable
    fun ImageCompose(
        painter: Painter,
        contentDescription: String,
        title: String,
        modifier: Modifier = Modifier
    ) {
        Card(
            modifier = modifier.fillMaxWidth(),
            shape = RoundedCornerShape(15.dp),
            elevation = 5.dp
        ) {
            Box(modifier = Modifier.height(200.dp)) {
                Image(
                    painter = painter,
                    contentDescription = contentDescription,
                    contentScale = ContentScale.Crop
                )
                Box(
                    modifier = Modifier
                        .fillMaxSize()
                        .background(
                            Brush.verticalGradient(
                                colors = listOf(
                                    Color.Transparent,
                                    Color.Black
                                ),
                                startY = 300f
                            )
                        )
                )
                Box(
                    modifier = Modifier
                        .fillMaxSize()
                        .padding(12.dp),
                    contentAlignment = Alignment.BottomStart
                ) {
                    Text(text = title, style = TextStyle(Color.White, fontSize = 16.sp))
                }
            }
        }
    }
}


